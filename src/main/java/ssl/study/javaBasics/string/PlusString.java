package ssl.study.javaBasics.string;

public class PlusString {
        static String x="1";
        static int y=1;
        public static void main(String args[]) {
            int z=2;
            //字符从左往右加
            System.out.println(x+y+z);
            //整数型从左往右加
            System.out.println(z+y+x);
        }
}
